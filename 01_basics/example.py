# 2. 变量与赋值
# ============================================================

# 基本赋值
name = "Alice"
age = 20
height = 1.68
is_student = True

print("姓名:", name)
print("年龄:", age)
print("身高:", height)
print("是否学生:", is_student)

# 多重赋值 —— 同时为多个变量赋相同的值
x = y = z = 0
print(f"\nx={x}, y={y}, z={z}")

# 同时赋不同的值（元组解包）
a, b, c = 1, 2, 3
print(f"a={a}, b={b}, c={c}")

# 交换两个变量（Python 特有的简洁写法）
a, b = b, a
print(f"交换后: a={a}, b={b}")

# ============================================================
# 3. print() 格式化输出
# ============================================================

print("\n--- print 格式化 ---")

# 普通输出，sep 指定分隔符，end 指定结尾
print("Python", "3.10", sep="-", end="!\n")

# f-string（Python 3.6+，最推荐的格式化方式）
score = 95.678
print(f"姓名: {name}, 成绩: {score:.2f}")  # 保留两位小数

# str.format()
print("姓名: {}, 成绩: {:.2f}".format(name, score))

# % 格式化（旧式，了解即可）
print("姓名: %s, 成绩: %.2f" % (name, score))

