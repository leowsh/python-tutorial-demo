# ============================================================
# 5. 类型转换
# ============================================================

print("\n--- 类型转换 ---")

# int() —— 转为整数
num_str = "42"
num_int = int(num_str)
print(f"字符串 '{num_str}' 转整数: {num_int}, 类型: {type(num_int)}")

# float() —— 转为浮点数
num_float = float(num_str)
print(f"字符串 '{num_str}' 转浮点数: {num_float}, 类型: {type(num_float)}")

# str() —— 转为字符串
num = 100
num_str2 = str(num)
print(f"整数 {num} 转字符串: '{num_str2}', 类型: {type(num_str2)}")

# bool() —— 转为布尔值
# 以下值转换为 False：0, 0.0, "", [], {}, None
print(f"bool(0) = {bool(0)}")
print(f"bool('') = {bool('')}")
print(f"bool(42) = {bool(42)}")
print(f"bool('hello') = {bool('hello')}")

# ============================================================
# 6. type() 查看类型
# ============================================================

print("\n--- type() 查看变量类型 ---")

values = [42, 3.14, "hello", True, None, [1, 2], {"key": "val"}]
for v in values:
    print(f"  值: {repr(v):20s}  类型: {type(v).__name__}")
